

// console.log()，该方法会打印传入到控制台的字符串

console.log('我的%s已经%d岁', '猫', 2)
// %s 会格式化变量为字符串
// %d 会格式化变量为数字
// %i 会格式化变量为其整数部分
// %o 会格式化变量为对象

// console.clear() 会清除控制台

// console.count() 会对打印的字符串的次数进行计数，并在其旁边打印计数

// console.trace() 打印函数的调用堆栈踪迹

// 使用 console.time() 和 console.timeEnd() 轻松地计算函数运行所需的时间

// console.log 非常适合在控制台中打印消息。 这就是所谓的标准输出（或称为 stdout）
// console.error 会打印到 stderr 流。




